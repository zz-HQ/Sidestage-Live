class SearchQuery < ActiveRecord::Base

  include Sortable

  #
  # Settings
  # ---------------------------------------------------------------------------------------
  #
  #
  #
  #
  
  sortable :location

  #
  # Attributes
  # ---------------------------------------------------------------------------------------
  #
  #
  #
  #
  
  store :content, accessors: [ :query ]
  
  #
  # Validations
  # ---------------------------------------------------------------------------------------
  #
  #
  #
  #

  validates :query, presence: true
  
  #
  # Scopes
  # ---------------------------------------------------------------------------------------
  #
  #
  #
  #
  
  scope :latest, -> { order("id DESC") }
    
  #
  # Private
  # ---------------------------------------------------------------------------------------
  #
  #
  #
  #
  
  private
  
end
